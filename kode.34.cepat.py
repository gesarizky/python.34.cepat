# Menginput
jarak = float(input("Tuliskan Berapa Kilometer Jarak Temput: "))
waktu = float(input("Tuliskan Berapa Jam Yang dibutuhkan: "))

# Mengkonversi
cepat = jarak / waktu

# Menampilkan Hasil
print('==========================================================')
print('Maka Kecepatan = %0.2f kilometer/Jam'  %(cepat))
print('==========================================================')
